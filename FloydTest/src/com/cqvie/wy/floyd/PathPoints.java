package com.cqvie.wy.floyd;

public class PathPoints {

	private String A;
	private String B;

	public PathPoints(String A, String B) {
		super();
		this.A = A;
		this.B = B;
	}

	public PathPoints() {
		
	}

	public String getA() {
		return A;
	}

	public void setA(String x) {
		A = x;
	}

	public String getB() {
		return B;
	}

	public void setB(String y) {
		B = y;
	}

}
